const transferAddress = '0x54eb390743a314ae8ad0484f5d5fa77794c0864a';
const smartContract = web3.eth.contract(abi).at(transferAddress);
function init() {
  // 첫번째 계정으로 주소 설정
  document.getElementById('address').value = web3.eth.accounts[0];
}

function ercTransfer() {
  var fromAddress = document.getElementById('address').value;
  var toAddress = document.getElementById('to_address').value;
  var amount = document.getElementById('to_amount').value;

  smartContract.transfer(
      toAddress,
      amount,
      { from: fromAddress, gas: 2000000 },
      (err, result) => {
        if (!err) alert('트랜잭션이 성공적으로 전송되었습니다.\n' + result);
      }
    );
}

App = {
    web3Provider: null,
  
    init: async function() {
      // Load pets.
      $.getJSON('../pets.json', function(data) {
        var petsRow = $('#petsRow');
        var petTemplate = $('#petTemplate');
  
        for (i = 0; i < data.length; i ++) {
          petTemplate.find('.panel-title').text(data[i].name);
          petTemplate.find('img').attr('src', data[i].picture);
          petTemplate.find('.goods').text(data[i].goods);
          petTemplate.find('.goods-use').text(data[i].use);
          petTemplate.find('.goods-cost').text(data[i].cost);
          petTemplate.find('.btn-adopt').attr('data-id', data[i].id);
          petTemplate.find('.pet-account').text(data[i].account);
  
          petsRow.append(petTemplate.html());
        }
      });
  
      //return await App.initWeb3();
    }
}

$(document).ready(function() {
  init();
  App.init();
  //showList();
});